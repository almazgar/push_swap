#include "libft.h"

int		ft_diglength(int n)
{
	int	l;

	if (n == -2147483648)
		return (11);
	l = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		l++;
	}
	while (n)
	{
		n = n / 10;
		l++;
	}
	return (l);
}

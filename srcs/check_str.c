#include "../includes/ft_push_swap.h"

int			check_str(char **s)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		while (s[i][j] != '\0')
		{
			if (s[i][j] >= 48 && s[i][j] <= 57)
				j++;
			else if ((s[i][j] == '-' || s[i][j] == '+') && s[i][j + 1]
				!= '\0' && s[i][j + 1] >= 48 && s[i][j + 1] <= 57)
				j++;
			else
				return (1);
		}
		if (plus_check(s[i]))
			return (1);
		j = 0;
		i++;
	}
	return (0);
}
